package lych.worldmodifiers.api.client.screen;

/**
 * Constants which represent colors used in World Modifier Mod's screens.
 */
public final class ColorConstants {
    /**
     * The color of the modifier's name shown in the confirm conflicts screen.
     */
    public static final int MODIFIER_NAME_COLOR = 0xCCCCCC;
    /**
     * The color of the modifier's value when the value is comparable and
     * is bigger than the default value.
     */
    public static final int MODIFIER_VALUE_INCREASED_COLOR = 0xD9FFDF;
    /**
     * The color of the modifier's value when the value is comparable and
     * is smaller than the default value.
     */
    public static final int MODIFIER_VALUE_DECREASED_COLOR = 0xD9F2FF;
}
